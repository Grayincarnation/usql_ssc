CREATE TABLE dbo.County
(
 CountyCode				VARCHAR(100) NOT NULL,
 CountyName				VARCHAR(100) NULL,
 NumberOfFootballTeams	INT NULL,
 AbbreviatedName		VARCHAR(10) NULL,
 AreaKMSquared			INT NULL,
 Population1991			INT NULL,
 Population2001			INT NULL,
 Population2011			INT NULL,
 Population2016			INT NULL,
 CONSTRAINT pk_County PRIMARY KEY CLUSTERED (CountyCode)
);

